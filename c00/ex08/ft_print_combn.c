void ft_print_combn(int n)
{
    int i;
    int num;
    int prenum;

    i = 0;
    num = 0;
    prenum = 0;
    
    while (i < n)
    {
        write(1, &(char){prenum + '0'}, 1);
        i++;
    }
    write(1, &(char){num + '0'}, 1);
    write(1, ", ", 2); 
}